/**
 * Returns the phase label for a given attribute name.
 * Attributes not listed in MVP or Phase 1 are considered Phase 2.
 */
export function getPhase(attribute) {
  if (MVP_ATTRS.has(attribute)) return "MVP";
  if (PHASE1_ATTRS.has(attribute)) return "Phase 1";
  return "Phase 2";
}

